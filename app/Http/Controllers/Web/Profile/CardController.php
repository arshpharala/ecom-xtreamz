     * Remove the specified resource from storage.
     */
    public function destroy(string $cardId, StripeService $stripe)
    {
        try {
            $stripe->deleteCardForUser(auth()->user(), $cardId);
            return response()->json(['success' => true]);
        } catch (\Exception $e) {
